    // Get number for a character
    public static int getNumber(char ch) {
        if (ch >= 'A' && ch <= 'Z') {
            return ch - 'A'; // A=0, B=1, ..., Z=25
        } else if (ch >= '0' && ch <= '9') {
            return 26 + (ch - '0'); // 0=26, 1=27, ..., 9=35
        } else if (ch == ' ') {
            return 36; // Space = 36
        } else {
            return -1; // Not allowed
        }
    }
    
    // Get character for a number
    public static char getChar(int num) {
        if (num >= 0 && num <= 25) {
            return (char) ('A' + num);
        } else if (num >= 26 && num <= 35) {
            return (char) ('0' + (num - 26));
        } else if (num == 36) {
            return ' ';
        } else {
            return '?'; // Not allowed
        }
    }
