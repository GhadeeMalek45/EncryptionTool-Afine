    private void enbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enbtnActionPerformed
        // TODO add your handling code here:
        crypt.setVisible(true);
         int m = 37;
         
         String text = PlainText.getText() ;
         String av = AValue.getText() ;
         String bv = BValue.getText() ;
         
         int a = Integer.parseInt(av) ;
         int b = Integer.parseInt(bv) ;
         
          // Encrypt
        String cipher = "";
        for (int i = 0; i < text.length(); i++) {
            char ch = text.charAt(i);
            int x = getNumber(ch);
            int enc = (a * x + b) % m;
            cipher = cipher + getChar(enc);
        }
        
        labeling.setText("Encrypted Text:");
        output.setText(cipher);
       
        
        
    }//GEN-LAST:event_enbtnActionPerformed

    private void debtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_debtnActionPerformed
        // TODO add your handling code here:
        crypt.setVisible(true);
         int m = 37;
         
         String text = PlainText.getText() ;
         String av = AValue.getText() ;
         String bv = BValue.getText() ;
         
         int a = Integer.parseInt(av) ;
         int b = Integer.parseInt(bv) ;
         
         // Find modular inverse of a
        int a_inv = -1;
        for (int i = 0; i < m; i++) {
            if ((a * i) % m == 1) {
                a_inv = i;
                break;
            }
        }
        
        // Decrypt
        String decrypted = "";
        for (int i = 0; i < text.length(); i++) {
            char ch = text.charAt(i);
            int y = getNumber(ch);
            int dec = (a_inv * (y - b + m)) % m;
            decrypted = decrypted + getChar(dec);
            
        }
        
        labeling.setText("Decrypted Text:");
        output.setText(decrypted);
    }//GEN-LAST:event_debtnActionPerformed

